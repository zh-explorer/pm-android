public class CityInfo {
    private String city;
    private String cnty;
    private String id;
    private String lat;
    private String lon;
    private String prov;

    public String getCity() {
        return city;
    }

    public String getCnty() {
        return cnty;
    }

    public String getId() {
        return id;
    }

    public String getLat() {
        return lat;
    }

    public String getLon() {
        return lon;
    }

    public String getProv() {
        return prov;
    }
}
